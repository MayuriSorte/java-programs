package basicconcept;//WAP on Arithmatic Operators " +,-, *, /, % " 

public class ArithmaticOperator {

	public static void main(String[] args) 
	{
		int age1=12;
		int age2=65;
		int age12=age1+age2;
		System.out.println(age12);
		
		long num1=67;
		long num2=56;
		long num3=num1-num2;
		System.out.println(num3);
		
		double mul1=23;
		double mul2=65.8;
		double mul3=mul1*mul2;
		System.out.println(mul3);
		
		float div1=87;
		float div2=34;
		float div3=div1/div2;
		System.out.println(div3);
		
		float mod1=76;
		float mod2=87;
		float mod3=mod1%mod2;
		System.out.println(mod3);
		
		float mod4=87;
		float mod5=76;
		float mod6=mod4%mod5;
		System.out.println(mod6);
		
		
	}

}
