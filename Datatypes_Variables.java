
package basicconcept;

public class Datatypes_Variables {

	public static void main(String[] args) {
		byte ageofkids;//declaration
		ageofkids=2; //Initialization
		System.out.println(ageofkids);
		
		byte age=10; //declaration and initialization both
		System.out.println(age);
		
		short a=6543;
		
		int b=656676;
		
		long  d=9876543;
		
		float e=2.35f;//can store upto 4 digit decimal value
		
		double f=3.14; //can store upto 19 digit long decimal value
				
		char gender ='f'; //capital or small anything, must be in single qyotes
		
		String name="Ram Tiwari"; //String value must be in Double quotes
		
		System.out.println(name);
		
		//byte is declaration, age is initialization, likewise for all
		//yellow lines are coming cause age and other variables are not utilized so just use it by adding sysout for printing
	}

}
