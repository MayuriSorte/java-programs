package basicconcept;//WAP on Access specifiers 
//outside the package by becoming the subclass for methods

public class Access_Specifier_Outside_Package1_Class 
{
	
	public static void Animal()
	{
		System.out.println("Animals");
	}
	protected static void Bird()
	{
		System.out.println("Birds");
	}
	private static void Plants()
	{
		System.out.println("Plants");
	}
	static void Tree()
	{
		System.out.println("Trees");
	}
	
}
